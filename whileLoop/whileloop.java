// whileloop.java

class whileloop {
	public static void main (String args[]) {
		int counter = 0;

		while(counter < 100000000) {
			// body of loop
			// this is the code that is repeted over and over as long as the paramenters are true.
			System.out.println(counter);
			
			//increment counter:
			counter++;
		}
	}
}

