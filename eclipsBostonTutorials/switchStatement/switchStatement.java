// switchStatement.java
public class switchStatement {
	public static void main (String args[]) {
		int age = 3;

		switch(age) {
			// this is the switch body.
			case 1: 
				System.out.println("You can crawl");
				break;
			case 2: 
				System.out.println("You can talk");
				break;
			case 3:
				System.out.println("you can get in trob!");
				break;
			default:
				System.out.println("You are not any years old");
				break;
		}

	}

}


