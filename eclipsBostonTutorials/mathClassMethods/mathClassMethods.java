package tutorials;

public class mathClassMethods {

	public static void main(String[] args) {
		
	// we are going to use our math methods and make some math things happen.
		
		// how many away from 0 are you?
		System.out.println(Math.abs(-845348.7));
		// round up
		System.out.println(Math.ceil(6.1));
		// round down
		System.out.println(Math.floor(6.9));
		// max: this returns the max of the two numbers.
		System.out.println(Math.max(6, 10));
		// min: gives the min of the two numbers:
		System.out.println(Math.min(6, 1));
		// power function:
		System.out.println(Math.pow(5, 3));
		// sqrt: finds the square root of a number:
		System.out.println(Math.sqrt(9));

	}
	

}
